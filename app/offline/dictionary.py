def lookup(word: str) -> dict | None:
    """Look up a word or phrase in the offline dictionary. Case-insensitive."""
    key = word.lower().strip()
    return FRENCH_OFFLINE.get(key)


def lookup_with_fallback(word: str, lemma: str | None = None) -> dict:
    """
    Look up word, then lemma, then return a minimal offline response.
    Never raises — always returns something useful.
    """
    result = lookup(word)
    if result:
        return {**result, "word": word, "lemma": lemma or word}

    if lemma and lemma != word:
        result = lookup(lemma)
        if result:
            return {**result, "word": word, "lemma": lemma}

    # Minimal fallback — at least return the word with empty fields
    return {
        "word": word,
        "lemma": lemma or word,
        "translation": "",
        "phonetic": "",
        "partOfSpeech": "Unknown",
        "cefrLevel": "B1",
        "frequencyRank": "unknown",
        "exampleSentence": "",
        "sentenceTranslation": "",
        "isIdiom": False,
        "isSlang": False,
        "explanation": "(offline — no AI provider available)",
    }


def search_idioms(text: str) -> list[dict]:
    """Find idioms that appear in a given text."""
    text_lower = text.lower()
    found = []
    for phrase, data in FRENCH_OFFLINE.items():
        if data.get("isIdiom") and phrase in text_lower:
            found.append({**data, "word": phrase, "lemma": phrase})
    return found
